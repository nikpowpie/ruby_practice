weather = 'sleeting'
case (weather)
when 'cloudy'
  puts "No sunscreen needed today!"
when 'sunny'
  puts "Make sure you wear sunscreen today!"
when 'foggy'
  puts "Put on your high beams when navigating the fog!"
when 'rainy'
  puts "Wear your rain coat today!"
else 
  puts "Hm, well, what do I look like? Your weather man?!"
end
